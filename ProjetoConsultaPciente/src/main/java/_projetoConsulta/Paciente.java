
package _projetoConsulta;


public class Paciente {
    private String cpf;
    private String nome;
    private String tele;

    
    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

   
    public String getNome() {
        return nome;
    }

    
   
    public void setNome(String nome) {
        this.nome = nome;
    }

   
    public String getTele() {
        return tele;
    }

  
    public void setTele(String tele) {
        this.tele = tele;
    }
    
    
   
 
    
    
}
